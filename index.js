let pwBoxOne = document.querySelector(".pw-box-1");
let pwBoxTwo = document.querySelector(".pw-box-2");

function randomChar() {
  let char = Math.floor(Math.random() * characters.length);
  return characters[char];
}

function generate() {
  let passwords = ["", ""];

  for (let i = 1; i < 15; i++) {
    passwords[0] += randomChar();
    passwords[1] += randomChar();
  }

  pwBoxOne.textContent = passwords[0];
  pwBoxTwo.textContent = passwords[1];
  return passwords;
}
